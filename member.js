// Function to check if a number is even or odd
function isEvenOrOdd(number) {
    if (number % 2 == 0) {
        console.log(number + " is even");
    } else {
        console.log(number + " is odd");
    }
}
isEvenOrOdd(6); // Output: 6 is even
isEvenOrOdd(7); // Output: 7 is odd